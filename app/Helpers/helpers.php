<?php
function kaz_translit($str){
    $kaz = array("ә","ң","ғ","ү","ұ","қ","ө","һ","Ә","Ң","Ғ","Ү","Ұ","Қ","Ө","Һ");
    $enc = array("&#1241;",
        "&#1187;",
        "&#1171;",
        "&#1199;",
        "&#1201;",
        "&#1179;",
        "&#1257;",
        "&#1211;",
        "&#1240;",
        "&#1186;",
        "&#1170;",
        "&#1198;",
        "&#1200;",
        "&#1178;",
        "&#1256;",
        "&#1210;"
    );
    $translit = str_replace($kaz, $enc, $str);
    return $translit;
}